numbers = [1, 2, 3, 4, 5]
#numbers[start:stop:step]
#numbers[-n:-m] == reverse order
#numbers=[-5,-4,-3,-2,-1]
#numbers[-3:-1] == 3 to 4
print(numbers)
print(numbers[-4:-1])
print("0 to 4:", numbers[0:4])  
print("1 to 4:", numbers[1:4]) 
print("2 to 5:", numbers[2:])
print("1 to 3:", numbers[:3])  
print("3 to 5:", numbers[2:5]) 
print("4 to 5:", numbers[3:5])
print("Every second element:", numbers[::2]) 

